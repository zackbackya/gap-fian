     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        //ddd($request);
        Message::create([
            'name' => $request->name,
            'email' => $request->email,
            'subject' => $request->subject,
            'message' => $request->message,
            'status' => 'F'
        ]);

        return redirect('/contact')->with('success','Your message has been sent. Thank you!');
    }

